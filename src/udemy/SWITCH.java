package udemy;

public class SWITCH {
public static void main(String[] args) {
	int num=5;
	switch(num) {
	case 1:if(num==1)
	System.out.println(num);
	case 2:if(num==2)
	System.out.println(num);
	case 3:if(num==3)
	System.out.println(num);
	case 4:if(num==4)
		System.out.println(num);
	default:	System.out.println("invalid");
	
	}
}
}
